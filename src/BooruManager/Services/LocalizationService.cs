    private static LocalizationService? _instance;
    public static LocalizationService Instance => _instance ??= new LocalizationService();

    private string _currentLanguage = "en";
    private readonly Dictionary<string, Dictionary<string, string>> _translations;

    public event Action? LanguageChanged;

    public string CurrentLanguage
    {
        get => _currentLanguage;
        set
        {
            if (string.IsNullOrEmpty(value))
            {
                return;
            }

            if (_currentLanguage != value && _translations != null && _translations.ContainsKey(value))
            {
                _currentLanguage = value;
                LanguageChanged?.Invoke();
            }
        }
    }

    public IReadOnlyList<LanguageInfo> AvailableLanguages { get; }

    private LocalizationService()
    {
        _translations = new Dictionary<string, Dictionary<string, string>>
        {
            ["en"] = GetEnglishStrings(),
            ["it"] = GetItalianStrings(),
            ["es"] = GetSpanishStrings(),
            ["fr"] = GetFrenchStrings(),
            ["de"] = GetGermanStrings(),
            ["pt"] = GetPortugueseStrings(),
            ["ja"] = GetJapaneseStrings(),
            ["zh"] = GetChineseStrings()
        };

        var systemLang = CultureInfo.CurrentUICulture.TwoLetterISOLanguageName.ToLowerInvariant();
        _currentLanguage = _translations.ContainsKey(systemLang) ? systemLang : "en";

        AvailableLanguages = new List<LanguageInfo>
        {
            new("en", "English"),
            new("it", "Italiano"),
            new("es", "Español"),
            new("fr", "Français"),
            new("de", "Deutsch"),
            new("pt", "Português"),
            new("ja", "日本語"),
            new("zh", "中文")
        };
    }

    public string this[string key] => GetString(key);

    public string GetString(string key)
    {
        if (_translations.TryGetValue(_currentLanguage, out var langStrings))
        {
            if (langStrings.TryGetValue(key, out var value))
            {
                return value;
            }
        }

        if (_translations.TryGetValue("en", out var enStrings))
        {
            if (enStrings.TryGetValue(key, out var value))
            {
                return value;
            }
        }

        return key;
    }

